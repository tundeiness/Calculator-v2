
const Computation = {
  '/': (operandOne, secondOperand) => operandOne / secondOperand,

  '*': (operandOne, secondOperand) => operandOne * secondOperand,

  '+': (operandOne, secondOperand) => operandOne + secondOperand,

  '-': (operandOne, secondOperand) => operandOne - secondOperand,

  '=': (operandOne, secondOperand) => secondOperand,
};


export default Computation;
