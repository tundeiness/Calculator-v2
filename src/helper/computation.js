const Computation = {
  '+': (operandOne, operandTwo) => operandOne + operandTwo,
  '-': (operandOne, operandTwo) => operandOne - operandTwo,
  'รท': (operandOne, operandTwo) => operandOne / operandTwo,
  '*': (operandOne, operandTwo) => operandOne * operandTwo,
  '=': (operandOne, operandTwo) => operandTwo,
};


export default Computation;
